/**
 * Generates functions/src/generated/firebaseConfig.ts from the repo-root
 * firebase.json so Cloud Functions share the same region/database config
 * as the client without hardcoded duplicates.
 *
 * Runs as part of `npm run build` (pre-tsc).
 */
const fs = require("fs");
const path = require("path");

const repoRoot = path.resolve(__dirname, "..", "..");
const firebaseJsonPath = path.join(repoRoot, "firebase.json");
const outDir = path.resolve(__dirname, "..", "src", "generated");
const outFile = path.join(outDir, "firebaseConfig.ts");

const raw = fs.readFileSync(firebaseJsonPath, "utf8");
const config = JSON.parse(raw);

const database = config?.firestore?.database;
const region = config?.firestore?.location;

if (!database || !region) {
  console.error(
    "[generate-firebase-config] firebase.json is missing firestore.database or firestore.location",
  );
  process.exit(1);
}

fs.mkdirSync(outDir, { recursive: true });
fs.writeFileSync(
  outFile,
  `// AUTO-GENERATED from /firebase.json — do not edit by hand.
// Regenerated by functions/scripts/generate-firebase-config.cjs.
export const DATABASE_ID = ${JSON.stringify(database)} as const;
export const REGION = ${JSON.stringify(region)} as const;
`,
);

console.log(
  `[generate-firebase-config] Wrote ${path.relative(repoRoot, outFile)} (region=${region}, db=${database})`,
);
